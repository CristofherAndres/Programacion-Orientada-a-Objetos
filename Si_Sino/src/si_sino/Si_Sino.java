package si_sino;
public class Si_Sino {
    public static void main(String[] args) {
        
        int a = 1;
        int b = 2;
        
    
        
        if (a > b){
            System.out.println("Verdadero toma A");
        }else{
            System.out.println("Falso toma B");
        }
        
        int resultado = a<b ? a : b;
        
        System.out.println("resultado ternaria: "+resultado);
            
        
        
        
    }
    
}
